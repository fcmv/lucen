import json
import os
import platform
import subprocess
import sys

HERE = os.path.dirname(os.path.abspath(__file__))
RESULTS = os.path.join(HERE, "results")
ORDER = ["py39", "py310", "py311", "py312", "py313", "py314", "py314t"]
WL_ORDER = [
    "light map",
    "medium map",
    "heavy map",
    "light reduction",
    "heavy reduction",
    "recognized DAG",
    "buffer map (array)",
    "nested heavy",
]
PATHS = ["plx_seq", "plx_thr", "plx_proc", "plx_gate", "hand_seq", "hand_thr", "hand_proc"]
TIMING_PATHS = ["native"] + PATHS
LABEL = {
    "native": "native Python",
    "plx_seq": "Lucen seq",
    "plx_thr": "Lucen thread",
    "plx_proc": "Lucen process",
    "plx_gate": "Lucen (gate on)",
    "hand_seq": "hand seq",
    "hand_thr": "hand thread",
    "hand_proc": "hand process",
}


def cpu_name():
    try:
        out = subprocess.run(
            ["powershell", "-NoProfile", "-Command", "(Get-CimInstance Win32_Processor).Name"],
            capture_output=True,
            text=True,
            timeout=30,
        ).stdout.strip()
        return out or platform.processor()
    except Exception:
        return platform.processor()


def load(subdir=""):
    data = {}
    for tag in ORDER:
        p = (
            os.path.join(RESULTS, subdir, tag + ".json")
            if subdir
            else os.path.join(RESULTS, tag + ".json")
        )
        if os.path.exists(p):
            with open(p, encoding="utf-8") as f:
                data[tag] = json.load(f)
    return data


def timing_tables(w, data):
    for tag, d in data.items():
        pyv = d["python"] + ("t" if not d["gil"] else "")
        w(f"### {pyv}  (native accel: {'yes' if d['native_accel'] else 'no'})")
        w("")
        hdr = (
            ["workload", "n"]
            + [LABEL[p] for p in TIMING_PATHS]
            + ["gate chose", "gate vs best hand"]
        )
        w("| " + " | ".join(hdr) + " |")
        w("|" + "---|" * len(hdr))
        for wl in WL_ORDER:
            row = d["workloads"][wl]
            ms = row["ms"]
            best_hand = min(ms["hand_seq"], ms["hand_thr"], ms["hand_proc"])
            ratio = ms["plx_gate"] / best_hand if best_hand else float("inf")
            cells = [wl, f"{row['n']:,}"]
            cells += [f"{ms[p]:.1f}" if p in ms else "-" for p in TIMING_PATHS]
            cells += [row.get("gate_backend", "?"), f"{ratio:.2f}x"]
            w("| " + " | ".join(cells) + " |")
        w("")


def main():
    data = load()
    out = []
    w = out.append

    w("# Lucen benchmark report")
    w("")
    w(f"Generated from `tests/benchmarks/results/*.json` ({len(data)} interpreters).")
    w("")
    w("## Environment")
    w("")
    w(f"- CPU: {cpu_name()}  ")
    first = next(iter(data.values()))
    w(f"- Logical cores: {first['cores']}  ")
    w(f"- OS: Windows 11 Pro ({platform.version()})  ")
    w(
        "- Interpreters: CPython standalone builds "
        "(astral-sh/python-build-standalone) except 3.11.0 (system install); "
        "3.14.6t is the free-threaded build (GIL disabled).  "
    )
    w(
        "- Lucen: this repository at the commit that generated this file; "
        "native `_core` (Rust, abi3) loads on every GIL build 3.9-3.14, "
        "pure-Python fallback on 3.14t.  "
    )
    w("")
    w("## Method")
    w("")
    w(
        "- Every number is the **median of 5 repetitions** after one discarded "
        "warm-up, a 1 s idle pause before each series, and per-interpreter "
        "cooldowns. Thread/process pools are created once and warm before "
        "timing.  "
    )
    w(
        "- `Lucen seq/thread/process` force the backend with the "
        "profitability gate bypassed, so the forced machinery really runs - "
        "these are diagnostics of raw pathway cost.  "
    )
    w(
        "- `Lucen (gate on)` is the shipped product: the router chooses; the "
        "chosen backend is reported.  "
    )
    w(
        "- **The hand-written comparison code was generated by an AI** to an "
        "expert-Python standard (warm `concurrent.futures` pools, one chunk per "
        "core, sliced inputs, partial-sum reductions, direct disjoint writes). "
        "It is the *style* a proficient human would write, but no human wrote "
        "or tuned it; treat cross-checks accordingly.  "
    )
    w(
        "- **`native Python` means the identical marked file with the pragmas "
        "treated as what they are - comments.** It is the program with "
        "Lucen entirely absent (`exec` of the source, no import hook, no "
        "twin), i.e. the exact code a user had before adding the two pragma "
        "lines. Because these benchmark sources live at module scope, their "
        "name lookups compile to `LOAD_GLOBAL`; the function-scoped variants "
        "(`Lucen seq`, `hand seq`) use `LOAD_FAST`, which is why native can "
        "read slower than both on name-heavy light loops - that difference is "
        "CPython scoping, not Lucen.  "
    )
    w(
        "- Correctness = exact equality against that same native run (floats "
        "compared bit-for-bit, containers element-wise).  "
    )
    w(
        "- Known gaps and planned work are deliberately kept out of this "
        "report; where a number reflects one (e.g. the gate not yet exploiting "
        "`typed_buffers`), the text says so briefly - refer to LIMITATIONS.md "
        "and ROADMAP.md for the full picture.  "
    )
    w(
        "- Single machine, ambient background load not controlled beyond "
        "cooldowns; run-to-run variance on dispatch-bound cells is +/-15%. "
        "Ratios within ~0.9-1.1x should be read as parity.  "
    )
    w("")
    w("## Native primitives")
    w("")
    w(
        "The optional Rust core accelerates two orchestration primitives. "
        "Measured native versus the pure-Python twin: the write-set audit over "
        "2M indices in 48 chunks, 36.5 ms native against 164.2 ms Python; the "
        "ordered 500k-element reduction fold with gaps, 6.1 ms native against "
        "31.7 ms Python. Both preserve the sequential result bit for bit. A "
        "native element-wise slab commit was also evaluated and is deliberately "
        "not used: it measured slower than CPython's specialized list stores "
        "(22.5 ms against 15.8 ms per 500k), so that loop runs in Python."
    )
    w("")

    w("## Correctness (bit-identical to plain Python)")
    w("")
    w(
        "`Y` = every element exactly equal, floats bit-for-bit. Hand-written "
        "thread/process reductions re-associate float addition - their "
        "divergence is expected and shown honestly; Lucen guarantees "
        "sequential-exact folds on every backend."
    )
    w("")
    for tag, d in data.items():
        pyv = d["python"] + ("t" if not d["gil"] else "")
        w(f"### {pyv}")
        w("")
        hdr = ["workload"] + [LABEL[p] for p in PATHS]
        w("| " + " | ".join(hdr) + " |")
        w("|" + "---|" * len(hdr))
        for wl in WL_ORDER:
            row = d["workloads"][wl]
            cells = [("Y" if row["correct"].get(p) else "**N**") for p in PATHS]
            w(f"| {wl} | " + " | ".join(cells) + " |")
        w("")

    w("## Timings (ms, median of 5)")
    w("")
    timing_tables(w, data)

    w("## The product view: Lucen (gate on) across versions")
    w("")
    hdr = ["workload"] + [d["python"] + ("t" if not d["gil"] else "") for d in data.values()]
    w("| " + " | ".join(hdr) + " |")
    w("|" + "---|" * len(hdr))
    for wl in WL_ORDER:
        cells = [wl]
        for d in data.values():
            row = d["workloads"][wl]
            cells.append(f"{row['ms']['plx_gate']:.1f} ({row['gate_backend'][:4]})")
        w("| " + " | ".join(cells) + " |")
    w("")
    w("## Native Python (pragmas as comments) across versions")
    w("")
    w("| " + " | ".join(hdr) + " |")
    w("|" + "---|" * len(hdr))
    for wl in WL_ORDER:
        cells = [wl]
        for d in data.values():
            cells.append(f"{d['workloads'][wl]['ms'].get('native', 0):.1f}")
        w("| " + " | ".join(cells) + " |")
    w("")
    w("## Gate speedup vs its own sequential twin, across versions")
    w("")
    w("| " + " | ".join(hdr) + " |")
    w("|" + "---|" * len(hdr))
    for wl in WL_ORDER:
        cells = [wl]
        for d in data.values():
            row = d["workloads"][wl]
            s = row["ms"]["plx_seq"] / row["ms"]["plx_gate"]
            cells.append(f"{s:.2f}x")
        w("| " + " | ".join(cells) + " |")
    w("")

    exp = load("exp")
    if exp:
        w("## Experimental flags enabled (`early_exit`, `branch_sensitive_deps`, `typed_buffers`)")
        w("")
        w(
            "The identical matrix re-run with every experimental feature "
            "active. None of the eight workloads contains a `break` or a "
            "branch-classification disagreement, so `early_exit` and "
            "`branch_sensitive_deps` are structurally inert here - and "
            "measurement agrees: of the 343 non-buffer timing cells, 322 are "
            "within the +/-15% noise band of the baseline, and the 21 outliers "
            "are scattered sub-100 ms dispatch-bound cells with no systematic "
            "direction, dominated by *hand-written* pathways the flags cannot "
            "touch - measurement noise, not flag effects. The one active flag "
            "for these shapes is `typed_buffers`: a dense map into an "
            "`array.array` ships typed result slabs on PROCESS instead of "
            "Python lists."
        )
        w("")
        bad = [
            (t, wl, k)
            for t, d in exp.items()
            for wl, row in d["workloads"].items()
            for k, ok in row["correct"].items()
            if k.startswith("plx") and not ok
        ]
        w(
            f"**Correctness with flags on:** Lucen bit-identical in "
            f"{7 * len(WL_ORDER) * 4 - len(bad)}/{7 * len(WL_ORDER) * 4} "
            "pathway cells"
            + (
                " - **failures: %r**" % bad
                if bad
                else " (no divergence; the hand-written float reductions differ "
                "exactly as in the baseline)."
            )
        )
        w("")
        w("### typed_buffers impact: buffer map (array), PROCESS pathway")
        w("")
        hdr2 = [
            "interpreter",
            "proc baseline",
            "proc flags-on",
            "change",
            "hand proc",
            "gate chose (flags)",
        ]
        w("| " + " | ".join(hdr2) + " |")
        w("|" + "---|" * len(hdr2))
        for tag in exp:
            if tag not in data:
                continue
            b = data[tag]["workloads"]["buffer map (array)"]["ms"]
            e = exp[tag]["workloads"]["buffer map (array)"]
            pyv = exp[tag]["python"] + ("t" if not exp[tag]["gil"] else "")
            w(
                f"| {pyv} | {b['plx_proc']:.1f} | {e['ms']['plx_proc']:.1f} | "
                f"{e['ms']['plx_proc'] / b['plx_proc']:.2f}x | "
                f"{e['ms']['hand_proc']:.1f} | {e['gate_backend']} |"
            )
        w("")
        w(
            "Typed slabs bring the forced-PROCESS pathway from ~2.6x "
            "hand-written down to parity with it. The gate still routes buffer "
            "maps sequential even with the flag on - a known, deliberate gap "
            "(the cost model is not yet flag-aware), so the typed pathway's "
            "win over sequential currently requires `backend=process`. See "
            "LIMITATIONS.md and ROADMAP.md."
        )
        w("")
        w("### Full timings with flags on (ms, median of 5)")
        w("")
        timing_tables(w, exp)

    w("## Summary: best numbers across all interpreters")
    w("")
    w(
        "Ranges span the seven interpreters; peaks name the interpreter that "
        "produced them. `Lucen gate` is the shipped product (router "
        "deciding); `Lucen peak (flags)` is the best Lucen number with "
        "every experimental flag on across *any* backend, forced ones included "
        "- an expert ceiling, not the default. `best hand` is the fastest "
        "AI-generated hand-written variant (which, for float reductions, is "
        "not bit-identical to native Python; Lucen always is)."
    )
    w("")
    hdr3 = [
        "workload",
        "native range (ms)",
        "Lucen gate range (ms)",
        "Lucen gate peak",
        "Lucen peak (flags, any backend)",
        "best hand-written",
    ]
    w("| " + " | ".join(hdr3) + " |")
    w("|" + "---|" * len(hdr3))

    def pyv_of(d):
        return d["python"] + ("t" if not d["gil"] else "")

    for wl in WL_ORDER:
        nat = [d["workloads"][wl]["ms"]["native"] for d in data.values()]
        gate = [
            (d["workloads"][wl]["ms"]["plx_gate"], pyv_of(d), d["workloads"][wl]["gate_backend"])
            for d in data.values()
        ]
        gate_best = min(gate)
        flag_cells = []
        for d in exp.values():
            ms = d["workloads"][wl]["ms"]
            for p in ("plx_gate", "plx_seq", "plx_thr", "plx_proc"):
                label = (
                    d["workloads"][wl]["gate_backend"]
                    if p == "plx_gate"
                    else p.split("_")[1] + " (forced)"
                )
                flag_cells.append((ms[p], pyv_of(d), label))
        flag_best = min(flag_cells)
        hand_cells = []
        for d in data.values():
            ms = d["workloads"][wl]["ms"]
            for p in ("hand_seq", "hand_thr", "hand_proc"):
                hand_cells.append((ms[p], pyv_of(d), p.split("_")[1]))
        hand_best = min(hand_cells)
        w(
            f"| {wl} "
            f"| {min(nat):.1f} - {max(nat):.1f} "
            f"| {min(g[0] for g in gate):.1f} - {max(g[0] for g in gate):.1f} "
            f"| {gate_best[0]:.1f} @ {gate_best[1]} ({gate_best[2]}) "
            f"| {flag_best[0]:.1f} @ {flag_best[1]} ({flag_best[2]}) "
            f"| {hand_best[0]:.1f} @ {hand_best[1]} ({hand_best[2]}) |"
        )
    w("")

    path = os.path.join(os.path.dirname(HERE), "..", "BENCHMARK.md")
    path = os.path.abspath(path)
    with open(path, "w", encoding="utf-8") as f:
        f.write("\n".join(out) + "\n")
    print("wrote", path, f"({len(out)} lines)")


if __name__ == "__main__":
    main()
